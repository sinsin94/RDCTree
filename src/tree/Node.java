package tree;


public class Node {

    //划分集合的时候，找到两个对象后，设置x,y;
    private Integer x;

    private Integer y;

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }
}
